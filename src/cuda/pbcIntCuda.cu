__global__
void ft_aopair_kernel(int ish0, int ish1, int jsh0, int jsh1,
                      double *outR, double *outI, AFTIntEnvVars envs, 
                      AFTBoundsInfo bounds, int sliceOut)
{
    // sp is short for shl_pair
    int sp_block_id = blockIdx.x;
    int Gv_block_id = blockIdx.y;
    int nGv_per_block = blockDim.x;
    int gout_stride = blockDim.y;
    int nsp_per_block = blockDim.z;
    int Gv_id_in_block = threadIdx.x;
    int gout_id = threadIdx.y;
    int sp_id = threadIdx.z;
    int npairs_ij = bounds.npairs_ij;
    int pair_ij = sp_block_id * nsp_per_block + sp_id;
    if (pair_ij >= npairs_ij) {
        return;
    }

    int nbas = envs.cell0_nbas * envs.bvk_ncells;
    int bas_ij = bounds.bas_ij_idx[pair_ij];
    int ish = bas_ij / nbas;
    int jsh = bas_ij % nbas;
    int *sp_img_offsets = bounds.img_offsets;
    int img0 = sp_img_offsets[pair_ij];
    int img1 = sp_img_offsets[pair_ij+1];

    int *atm = envs.atm;
    int *bas = envs.bas;
    double *env = envs.env;

    int li = bounds.li;
    int lj = bounds.lj;
    int nfij = bounds.nfij;
    //int iprim = bounds.iprim;
    //int jprim = bounds.jprim;
    int iprim = bas[ish*BAS_SLOTS + NPRIM_OF];
    int jprim = bas[jsh*BAS_SLOTS + NPRIM_OF];

    int ijprim = iprim * jprim;
    int lij = li + lj;
    int stride_j = bounds.stride_j;
    int g_size = bounds.g_size;
    int gx_len = g_size * nGv_per_block * nsp_per_block;
    int16_t *idx_ij = c_pair_idx + c_pair_offsets[li*L_AUX1+lj];
    int16_t *idy_ij = idx_ij + nfij;
    int16_t *idz_ij = idy_ij + nfij;

    double *expi = env + bas[ish*BAS_SLOTS+PTR_EXP];
    double *expj = env + bas[jsh*BAS_SLOTS+PTR_EXP];
    double *ci = env + bas[ish*BAS_SLOTS+PTR_COEFF];
    double *cj = env + bas[jsh*BAS_SLOTS+PTR_COEFF];
    int ia = bas[ish*BAS_SLOTS+ATOM_OF];
    int ja = bas[jsh*BAS_SLOTS+ATOM_OF];
    double *ri = env + atm[ia*ATM_SLOTS+PTR_COORD];
    double *rj = env + atm[ja*ATM_SLOTS+PTR_COORD];
    double *img_coords = envs.img_coords;
    int *img_idx = bounds.img_idx;

    int nGv = bounds.ngrids;
    int Gv_id = Gv_block_id * nGv_per_block + Gv_id_in_block;
    double *Gv = bounds.grids + Gv_id;
    double kx = Gv[0];
    double ky = Gv[nGv];
    double kz = Gv[nGv * 2];
    double kk = kx * kx + ky * ky + kz * kz;
    double rjri[3];

    // the shape of shared tensor is (nsp, nGv, ng)
    extern __shared__ double g[];
    double *gxR = g + sp_id * nGv_per_block * g_size + Gv_id_in_block;
    double *gxI = gxR + gx_len;
    double *gyR = gxI + gx_len;
    double *gyI = gyR + gx_len;
    double *gzR = gyI + gx_len;
    double *gzI = gzR + gx_len;

    double goutR[GOUT_WIDTH];
    double goutI[GOUT_WIDTH];
#pragma unroll
    for (int n = 0; n < GOUT_WIDTH; ++n) {
        goutR[n] = 0.;
        goutI[n] = 0.;
    }
    double s0xR, s1xR, s2xR;
    double s0xI, s1xI, s2xI;

    for (int ijp = 0; ijp < ijprim; ++ijp) {
        int ip = ijp / jprim;
        int jp = ijp % jprim;
        double ai = expi[ip];
        double aj = expj[jp];
        double aij = ai + aj;
        double aj_aij = aj / aij;
        double theta_ij = ai * aj_aij;
        double a2 = .5 / aij;
        double fac = OVERLAP_FAC * ci[ip] * cj[jp] / (aij * sqrt(aij));

        for (int img = img0; img < img1; img++) {
            int img_id = img_idx[img];
            double Lx = img_coords[img_id*3+0];
            double Ly = img_coords[img_id*3+1];
            double Lz = img_coords[img_id*3+2];
            double xjxi = rj[0] + Lx - ri[0];
            double yjyi = rj[1] + Ly - ri[1];
            double zjzi = rj[2] + Lz - ri[2];
            rjri[0] = xjxi;
            rjri[1] = yjyi;
            rjri[2] = zjzi;

            __syncthreads();
            if (gout_id == 0) {
                double xij = xjxi * aj_aij + ri[0];
                double yij = yjyi * aj_aij + ri[1];
                double zij = zjzi * aj_aij + ri[2];
                double kR = kx * xij + ky * yij + kz * zij;
                sincos(-kR, gzI, gzR);
                double rr = xjxi*xjxi + yjyi*yjyi + zjzi*zjzi;
                double theta_rr = theta_ij*rr + .5*a2*kk;
                double Kab = exp(-theta_rr);
                gxR[0] = fac;
                gxI[0] = 0.;
                gyR[0] = 1.;
                gyI[0] = 0.;
                // exp(-theta_rr-kR*1j)
                gzR[0] *= Kab;
                gzI[0] *= Kab;
            }

            if (lij > 0) {
                // gx[i+1] = ia2 * gx[i-1] + (rijrx[0] - kx[n]*a2*_Complex_I) * gx[i];
                __syncthreads();
                for (int n = gout_id; n < 3; n += gout_stride) {
                    int gidx = n * gx_len * OF_COMPLEX;
                    double *_gxR = gxR + gidx;
                    double *_gxI = _gxR + gx_len;
                    //double *_gxI = gxI + gidx;
                    double RpaR = rjri[n] * aj_aij; // Rp - Ra
                    double RpaI = -a2 * Gv[nGv*n];
                    s0xR = _gxR[0];
                    s0xI = _gxI[0];
                    s1xR = RpaR * s0xR - RpaI * s0xI;
                    s1xI = RpaR * s0xI + RpaI * s0xR;
                    _gxR[nGv_per_block] = s1xR;
                    _gxI[nGv_per_block] = s1xI;

                    for (int i = 1; i < lij; i++) {
                        double ia2 = i * a2;
                        s2xR = ia2 * s0xR + RpaR * s1xR - RpaI * s1xI;
                        s2xI = ia2 * s0xI + RpaR * s1xI + RpaI * s1xR;
                        _gxR[(i+1)*nGv_per_block] = s2xR;
                        _gxI[(i+1)*nGv_per_block] = s2xI;
                        s0xR = s1xR;
                        s0xI = s1xI;
                        s1xR = s2xR;
                        s1xI = s2xI;
                    }
                }
            }

            // hrr
            if (lj > 0) {
                __syncthreads();
                for (int n = gout_id; n < 3*OF_COMPLEX; n += gout_stride) {
                    double *_gx = gxR + n * gx_len;
                    // The real and imaginary parts call the same expression
                    int _ix = n / 2;
                    double xjxi = rjri[_ix];
                    for (int j = 0; j < lj; ++j) {
                        int ij = (lij-j) + j*stride_j;
                        s1xR = _gx[ij*nGv_per_block];
                        for (--ij; ij >= j*stride_j; --ij) {
                            s0xR = _gx[ij*nGv_per_block];
                            _gx[(ij+stride_j)*nGv_per_block] = s1xR - xjxi * s0xR;
                            s1xR = s0xR;
                        }
                    }
                }
            }

            __syncthreads();
#pragma unroll
            for (int n = 0; n < GOUT_WIDTH; ++n) {
                int ij = n*gout_stride + gout_id;
                if (ij >= nfij) break;
                int addrx = idx_ij[ij] * nGv_per_block;
                int addry = idy_ij[ij] * nGv_per_block;
                int addrz = idz_ij[ij] * nGv_per_block;
                double xR = gxR[addrx];
                double xI = gxI[addrx];
                double yR = gyR[addry];
                double yI = gyI[addry];
                double zR = gzR[addrz];
                double zI = gzI[addrz];
                double xyR = xR * yR - xI * yI;
                double xyI = xR * yI + xI * yR;

                goutR[n] += xyR * zR - xyI * zI;
                goutI[n] += xyR * zI + xyI * zR;
            }
        }
    }

    if (Gv_id < nGv) {
    //if (0) {

        if (sliceOut) {
            int nbasp = envs.cell0_nbas;
            size_t cell_id = jsh / nbasp;
            int cell0_jsh = jsh % nbasp;

            int nfi = (li + 1) * (li + 2) / 2;
            int nfj = (lj + 1) * (lj + 2) / 2;
            int nfij = nfi * nfj;
            int nao_i = (ish1 - ish0) * nfi;
            int nao_j = (jsh1 - jsh0) * nfj;
            int iao0 = (ish - ish0) * nfi;
            int jao0 = (cell0_jsh - jsh0) * nfj;
            //double *aft_tensor = out + (cell_id * nao_i*nao_j*nGv) * OF_COMPLEX;
            size_t idx0 = cell_id * nao_i*nao_j*nGv;
            double* aftReal = outR + idx0;
            double* aftImag = outI + idx0;
#pragma unroll
            for (int n = 0; n < GOUT_WIDTH; ++n) {
                int ij = n*gout_stride + gout_id;
                if (ij >= nfij) break;
                size_t i = ij % nfi;
                size_t j = ij / nfi;
                /*
                size_t idx = (((iao0 + i) * nao_j + (jao0 + j)) * nGv + Gv_id) * OF_COMPLEX;
                aft_tensor[idx  ] = goutR[n];
                aft_tensor[idx+1] = goutI[n];*/
                size_t addr = ((iao0 + i) * nao_j + (jao0 + j)) * nGv + Gv_id;
                aftReal[addr] = goutR[n];
                aftImag[addr] = goutI[n];
            }

        } else {
            int nfi = (li + 1) * (li + 2) / 2;
            int *ao_loc = envs.ao_loc;
            int nbasp = envs.cell0_nbas;
            size_t nao = ao_loc[nbasp];
            size_t cell_id = jsh / nbasp;
            int cell0_jsh = jsh % nbasp;
            size_t i0 = ao_loc[ish];
            size_t j0 = ao_loc[cell0_jsh];
            /*
            double *aft_tensor = out +
                    (cell_id * nao*nao*nGv + (i0*nao+j0) * nGv + Gv_id) * OF_COMPLEX;*/
            
            size_t idx0 = cell_id * nao*nao*nGv + (i0*nao+j0) * nGv + Gv_id;
            double* aftReal = outR + idx0;
            double* aftImag = outI + idx0;
#pragma unroll
            for (int n = 0; n < GOUT_WIDTH; ++n) {
                int ij = n*gout_stride + gout_id;
                if (ij >= nfij) break;
                size_t i = ij % nfi;
                size_t j = ij / nfi;
                size_t addr = (i*nao+j)*nGv;
                /*
                aft_tensor[addr*2  ] = goutR[n];
                aft_tensor[addr*2+1] = goutI[n];*/

                aftReal[addr] = goutR[n];
                aftImag[addr] = goutI[n];

                
            }
        }
    }
}



__global__
void ftAoBdivKernel(double *outR, double *outI, AFTIntEnvVars envs, int nGv, double *grids)
{
    int sh_block_id = gridDim.x - blockIdx.x - 1;
    int Gv_block_id = blockIdx.y;
    int nsh_per_block = FT_AO_THREADS / NG_PER_BLOCK;
    int sh_id_in_block = threadIdx.y;
    int Gv_id_in_block = threadIdx.x;
    int sh_id = sh_block_id * nsh_per_block + sh_id_in_block;

    if (sh_id >= envs.cell0_nbas) {
        return;
    }

    int *atm = envs.atm;
    int *bas = envs.bas;
    double *env = envs.env;
    int li = bas[sh_id*BAS_SLOTS+ANG_OF];
    int nfi = (li + 1) * (li + 2) / 2;
    int16_t *idx = c_pair_idx + nfi * li;
    int16_t *idy = idx + nfi;
    int16_t *idz = idy + nfi;
    int iprim = bas[sh_id*BAS_SLOTS+NPRIM_OF];

    int Gv_id = Gv_block_id * NG_PER_BLOCK + Gv_id_in_block;
    double *Gv = grids + Gv_id;
    double kx = Gv[0];
    double ky = Gv[nGv];
    double kz = Gv[nGv * 2];
    double kk = kx * kx + ky * ky + kz * kz;

    int gx_len = (AUXL+1) * FT_AO_THREADS;
    __shared__ double g[(AUXL+1)*FT_AO_THREADS * 6];
    int idx0 =  (AUXL+1)*FT_AO_THREADS * 6;
    int idx1 = (AUXL+1) * NG_PER_BLOCK * sh_id_in_block + Gv_id_in_block + gx_len*5;
    
    double *gxR = g + (AUXL+1) * NG_PER_BLOCK * sh_id_in_block + Gv_id_in_block;
    double *gxI = gxR + gx_len;
    double *gyR = gxI + gx_len;
    double *gyI = gyR + gx_len;
    double *gzR = gyI + gx_len;
    double *gzI = gzR + gx_len;

    double goutR[AUXNF];
    double goutI[AUXNF];
#pragma unroll
    for (int n = 0; n < AUXNF; ++n) {
        goutR[n] = 0.;
        goutI[n] = 0.;
    }
    double s0xR, s1xR, s2xR;
    double s0xI, s1xI, s2xI;
    double s0yR, s1yR, s2yR;
    double s0yI, s1yI, s2yI;
    double s0zR, s1zR, s2zR;
    double s0zI, s1zI, s2zI;

    int ia = bas[sh_id*BAS_SLOTS+ATOM_OF];
    double *expi = env + bas[sh_id*BAS_SLOTS+PTR_EXP];
    double *ci = env + bas[sh_id*BAS_SLOTS+PTR_COEFF];
    double *ri = env + atm[ia*ATM_SLOTS+PTR_COORD];

    
    for (int ip = 0; ip < iprim; ++ip) {
        __syncthreads();
        double ai = expi[ip];
        double xi = ri[0];
        double yi = ri[1];
        double zi = ri[2];
        double kR = kx * xi + ky * yi + kz * zi;
        sincos(-kR, gzI, gzR);
        double Kab = exp(-.25/ai*kk);
        gxR[0] = OVERLAP_FAC * ci[ip] / (ai * sqrt(ai));
        gxI[0] = 0.;
        gyR[0] = 1.;
        gyI[0] = 0.;
        gzR[0] *= Kab;
        gzI[0] *= Kab;

        if (li > 0) {
            double a2 = .5 / ai;
            double xpaI = -a2 * kx;
            double ypaI = -a2 * ky;
            double zpaI = -a2 * kz;
            s0xR = gxR[0];
            s0xI = gxI[0];
            s0yR = gyR[0];
            s0yI = gyI[0];
            s0zR = gzR[0];
            s0zI = gzI[0];
            s1xR = -xpaI * s0xI;
            s1xI =  xpaI * s0xR;
            s1yR = -ypaI * s0yI;
            s1yI =  ypaI * s0yR;
            s1zR = -zpaI * s0zI;
            s1zI =  zpaI * s0zR;
            gxR[NG_PER_BLOCK] = s1xR;
            gxI[NG_PER_BLOCK] = s1xI;
            gyR[NG_PER_BLOCK] = s1yR;
            gyI[NG_PER_BLOCK] = s1yI;
            gzR[NG_PER_BLOCK] = s1zR;
            gzI[NG_PER_BLOCK] = s1zI;
            for (int i = 2; i <= AUXL; i++) {
                if (i > li) break;
                double ia2 = (i-1) * a2;
                s2xR = ia2 * s0xR - xpaI * s1xI;
                s2xI = ia2 * s0xI + xpaI * s1xR;
                s2yR = ia2 * s0yR - ypaI * s1yI;
                s2yI = ia2 * s0yI + ypaI * s1yR;
                s2zR = ia2 * s0zR - zpaI * s1zI;
                s2zI = ia2 * s0zI + zpaI * s1zR;
                gxR[i*NG_PER_BLOCK] = s2xR;
                gxI[i*NG_PER_BLOCK] = s2xI;
                gyR[i*NG_PER_BLOCK] = s2yR;
                gyI[i*NG_PER_BLOCK] = s2yI;
                gzR[i*NG_PER_BLOCK] = s2zR;
                gzI[i*NG_PER_BLOCK] = s2zI;
                s0xR = s1xR;
                s0xI = s1xI;
                s0yR = s1yR;
                s0yI = s1yI;
                s0zR = s1zR;
                s0zI = s1zI;
                s1xR = s2xR;
                s1xI = s2xI;
                s1yR = s2yR;
                s1yI = s2yI;
                s1zR = s2zR;
                s1zI = s2zI;
            }
        }

        
        __syncthreads();

        #pragma unroll
        for (int n = 0; n < AUXNF; ++n) {
            if (n >= nfi) break;
            int addrx = idx[n] * NG_PER_BLOCK;
            int addry = idy[n] * NG_PER_BLOCK;
            int addrz = idz[n] * NG_PER_BLOCK;
            double xR = gxR[addrx];
            double xI = gxI[addrx];
            double yR = gyR[addry];
            double yI = gyI[addry];
            double zR = gzR[addrz];
            double zI = gzI[addrz];
            double xyR = xR * yR - xI * yI;
            double xyI = xR * yI + xI * yR;
            goutR[n] += xyR * zR - xyI * zI;
            goutI[n] += xyR * zI + xyI * zR;
        }
    }

    if (Gv_id < nGv) {
        int stride = nGv; //* OF_COMPLEX;
        //double *aft_tensor = out + (envs.ao_loc[sh_id] * nGv + Gv_id) * OF_COMPLEX;
        size_t idx0 = envs.ao_loc[sh_id] * nGv + Gv_id;
        double *aftR = outR + idx0;
        double *aftI = outI + idx0;
        
#pragma unroll
        for (int n = 0; n < AUXNF; ++n) {
            if (n >= nfi) break;
            //aft_tensor[n*stride  ] = goutR[n];
            //aft_tensor[n*stride+1] = goutI[n];
            size_t idx = n*stride;
            aftR[idx] = goutR[n];
            aftI[idx] = goutI[n];
        }
    }
}

__global__ static
void overlap_img_counts_kernel(int *img_counts, int *p2c_mapping,
                               int ish0, int jsh0, int nish, int njsh,
                               PBCInt3c2eEnvVars envs, float *exps,
                               float *log_coeff, float log_cutoff, 
                               int permutation_symmetry)
{
    int bas_ij = blockIdx.x * blockDim.x + threadIdx.x;
    int bvk_nish = envs.bvk_ncells * nish;
    int bvk_njsh = envs.bvk_ncells * njsh;
    if (bas_ij >= bvk_nish*bvk_njsh) {
        return;
    }
    int nimgs = envs.nimgs;
    int *atm = envs.atm;
    int *bas = envs.bas;
    double *env = envs.env;
    double *img_coords = envs.img_coords;
    int ish = bas_ij / bvk_njsh;
    int jsh = bas_ij % bvk_njsh;
    int cell0_ish = ish % nish + ish0;;
    int cell0_jsh = jsh % njsh + jsh0;;
    if (permutation_symmetry && cell0_ish < cell0_jsh &&
        // filtering based on the contracted orbital-pairs than the primitive shells
        p2c_mapping[cell0_ish] != p2c_mapping[cell0_jsh]) {
        return;
    }
    ish = ish / nish * envs.cell0_nbas + cell0_ish;
    jsh = jsh / njsh * envs.cell0_nbas + cell0_jsh;
    int li = bas[ANG_OF + cell0_ish*BAS_SLOTS];
    int lj = bas[ANG_OF + cell0_jsh*BAS_SLOTS];
    float ai = exps[cell0_ish];
    float aj = exps[cell0_jsh];
    float aij = ai + aj;
    float fi = ai / aij;
    float fj = aj / aij;
    float theta_ij = ai * aj / aij;
    float log_ci = log_coeff[cell0_ish];
    float log_cj = log_coeff[cell0_jsh];
    float log_cicj = log_ci + log_cj;
    double *ri = env + atm[bas[ish*BAS_SLOTS+ATOM_OF] * ATM_SLOTS + PTR_COORD];
    double *rj = env + atm[bas[jsh*BAS_SLOTS+ATOM_OF] * ATM_SLOTS + PTR_COORD];
    float xi = ri[0];
    float yi = ri[1];
    float zi = ri[2];
    float xj = rj[0];
    float yj = rj[1];
    float zj = rj[2];
    // log(ci*cj * (pi/aij)**1.5)
    float log_fac = log_cicj + 1.717f - 1.5f*logf(aij);
    // An addiitonal factor for Coulomb integrals
    // log_fac += .25 * logf(2./pi * aij)
    log_fac += .25f * logf(0.6366f * aij);
    log_cutoff = log_cutoff - log_fac;

    int counts = 0;
    for (int img = 0; img < nimgs; ++img) {
        float xjL = xj + img_coords[img*3+0];
        float yjL = yj + img_coords[img*3+1];
        float zjL = zj + img_coords[img*3+2];
        float xjxi = xjL - xi;
        float yjyi = yjL - yi;
        float zjzi = zjL - zi;
        float rr_ij = xjxi * xjxi + yjyi * yjyi + zjzi * zjzi;
        float theta_ij_rr = theta_ij * rr_ij;
        if (theta_ij_rr > REMOTE_THRESHOLD) {
            continue;
        }

        float dr = sqrtf(rr_ij);
        float dri = fj * dr;
        float drj = fi * dr;
        float dri_fac = .5f*li * logf(.5f*li/aij + dri*dri + 1e-9f);
        float drj_fac = .5f*lj * logf(.5f*lj/aij + drj*drj + 1e-9f);
        float estimator = dri_fac + drj_fac - theta_ij_rr;
        if (estimator > log_cutoff) {
            counts++;
        }
    }
    img_counts[bas_ij] = counts;
}


extern "C" {

int bvk_overlap_img_counts(int *img_counts, int *p2c_mapping, int *shls_slice,
                           PBCInt3c2eEnvVars *envs, float *exps, float *log_coeff,
                           float log_cutoff, int permutation_symmetry)
{
    int ish0 = shls_slice[0];
    int ish1 = shls_slice[1];
    int jsh0 = shls_slice[2];
    int jsh1 = shls_slice[3];
    int nish = ish1 - ish0;
    int njsh = jsh1 - jsh0;
    constexpr int threads = 512;
    int ncells = envs->bvk_ncells;
    int blocks = (ncells*nish*ncells*njsh + threads-1)/threads;
    overlap_img_counts_kernel<<<blocks, threads>>>(
        img_counts, p2c_mapping, ish0, jsh0, nish, njsh,
        *envs, exps, log_coeff, log_cutoff, permutation_symmetry);
    cudaError_t err = cudaGetLastError();
    if (err != cudaSuccess) {
        fprintf(stderr, "CUDA Error in bvk_overlap_img_counts: %s\n", cudaGetErrorString(err));
        return 1;
    }
    return 0;
}


